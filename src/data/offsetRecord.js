const deal_data = (rowdata) => {
  let data = []
  for (let rowObject of rowdata) {
    let object = {};
    object.date = rowObject.date;
    object.Name = rowObject.Name;
    object.SubTotal = rowObject.SubTotal;
    let array = [];
    const rowArray = rowObject.Array;
    for (const row of rowArray) {

      if (row.BidAsk === '买') {
      let record1 = {};
      record1.sell = '卖';
      record1.price = row.OpenPrice;
      record1.OpenDate = row.OpenDate;
      record1.ExchangeNo = row.ExchangeNo;
      record1.Contract = row.Contract;

      record1.Vol = '';
      record1.buy = '';
      record1.AccumPL = '';
      record1.MTMPL = '';
      record1.Fee = '';
      record1.Hedge = '';
      record1.PremiumIO = '';


      let record2 = {};
      record2.buy = '买';
      record2.Vol = row.Vol;
      record2.price = row.MatchPrice;
      record2.AccumPL = row.AccumPL;
      record2.MTMPL = row.MTMPL;
      record2.Fee = row.Fee;
      record2.Hedge = row.Hedge;
      record2.PremiumIO = row.PremiumIO;

      record2.OpenDate = '';
      record2.ExchangeNo = '';
      record2.Contract = '';
      record2.sell = '';

      array.push(record1);
      array.push(record2);

      } else {
      let record1 = {};
      record1.OpenDate = row.OpenDate;
      record1.ExchangeNo = row.ExchangeNo;
      record1.Contract = row.Contract;
      record1.price = row.OpenPrice;
      record1.buy = '买';

      record1.Vol = '';
      record1.sell = '';
      record1.AccumPL = '';
      record1.MTMPL = '';
      record1.Fee = '';
      record1.Hedge = '';
      record1.PremiumIO = '';


      let record2 = {};
      record2.sell = '卖';
      record2.Vol = row.Vol;
      record2.price = row.MatchPrice;
      record2.AccumPL = row.AccumPL;
      record2.MTMPL = row.MTMPL;
      record2.Fee = row.Fee;
      record2.Hedge = row.Hedge;
      record2.PremiumIO = row.PremiumIO;

      record2.OpenDate = '';
      record2.ExchangeNo = '';
      record2.Contract = '';
      record2.buy = '';


      array.push(record1);
      array.push(record2);
      }
      
    }
    object.Array = array;
    data.push(object);
  }
  return data;
}

